CREATE DATABASE cursores2;
USE cursores2;

-- Tabla de tiendas
CREATE TABLE tiendas (
    id_tienda INT PRIMARY KEY,
    nombre_tienda VARCHAR(50),
    ubicacion VARCHAR(100)
)ENGINE=InnoDB;

-- Tabla de ventasDESC
CREATE TABLE ventas (
    id_venta INT PRIMARY KEY,
    id_producto INT,
    id_tienda INT,
    fecha_venta DATE,
    cantidad INT,
    FOREIGN KEY (id_tienda) REFERENCES tiendas(id_tienda)
)ENGINE=InnoDB;

